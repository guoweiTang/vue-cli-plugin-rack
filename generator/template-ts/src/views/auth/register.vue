<template>
  <teleport to="#app">
    <div class="auth">
      <div class="modal-box">
        <div class="modal-header">
          <dl class="auth-logo">
            <dt>
              <img
                src="../../assets/logo.png"
                alt="logo"
                width="34"
                height="34"
              />
            </dt>
            <dd>VUE RACK</dd>
          </dl>
          <h2>注册</h2>
          <p>创建你的免费账户</p>
        </div>
        <el-form
          label-position="top"
          :model="formData"
          :rules="rules"
          ref="ruleForm"
          label-width="100px"
          class="demo-ruleForm"
          @keyup.enter="submitForm"
        >
          <el-form-item label="邮箱" prop="email">
            <el-input
              v-model.trim="formData.email"
              autocomplete="off"
            ></el-input>
          </el-form-item>
          <el-form-item label="密码" prop="password">
            <el-input
              show-password
              type="password"
              v-model="formData.password"
              autocomplete="off"
            ></el-input>
          </el-form-item>
          <el-form-item label="确认密码" prop="checkPassword">
            <el-input
              show-password
              type="password"
              v-model="formData.checkPassword"
              autocomplete="off"
            ></el-input>
          </el-form-item>
          <el-form-item>
            <el-button
              type="primary"
              @click="submitForm"
              :loading="loading"
              >注册</el-button
            >
          </el-form-item>
          <div class="no-acoout">
            已有账户？<router-link :to="{ name: 'login' }"
              ><el-link type="primary" :underline="false"
                >去登录</el-link
              ></router-link
            >
          </div>
        </el-form>
      </div>
    </div>
  </teleport>
</template>

<script lang="ts">
import { Options, Vue } from 'vue-class-component';
import { emailPattern } from '@/config';
import { register } from '@/views/service';

@Options({
  name: 'Register',
})
export default class extends Vue {
  private validEmail = (rule: any, value: string, callback: (arg0: Error|undefined) => void) => {
    if (!emailPattern.test(value)) {
      callback(new Error('请输入正确的邮箱'));
    } else {
      callback(undefined);
    }
  }

  private validCheckPass = (rule: any, value: any, callback: (arg0: Error|undefined) => void) => {
    if (value !== (this as any).ruleForm.password) {
      callback(new Error('两次输入密码不一致!'));
    } else {
      callback(undefined);
    }
  }

  // 是否表单提交中
  private loading = false

  // 表单值
  private formData = {
    email: '',
    password: '',
    checkPassword: '',
  }

  private rules = {
    email: [
      { required: true, message: '请输入邮箱', trigger: 'blur' },
      { validator: this.validEmail, trigger: 'blur' },
    ],
    password: [
      { required: true, message: '请输入密码', trigger: 'blur' },
      { min: 3, message: '密码至少为3个字符', trigger: 'blur' },
    ],
    checkPassword: [
      { required: true, message: '请再次输入密码', trigger: 'blur' },
      { min: 3, message: '再次输入密码至少为3个字符', trigger: 'blur' },
      { validator: this.validCheckPass, trigger: 'blur' },
    ],
  }

  // 表单提交
  private submitForm() {
    (this as any).$refs.ruleForm.validate((valid: any) => {
      if (valid) {
        this.loading = true;
        register({ router: this.$router, data: this.formData })
          .then(() => {
            this.loading = false;
            (this as any).$message.success({
              duration: 1000,
              message: '注册成功，请继续登录',
              onClose: () => {
                this.$router.push({
                  name: 'login',
                });
              },
            });
          })
          .catch(() => {
            this.loading = false;
          });
      } else {
        console.log('error submit!!');
      }
    });
  }
}
</script>
